export function getFloorMessage(floor: number): string {
  const specific = FLOOR_MESSAGES[floor];
  if (specific) {
    return specific[Math.floor(Math.random() * specific.length)];
  }
  // Generic messages for floors without specific text
  const generic = [
    '階段を降りた。空気が一段と重くなった。',
    '壁のひび割れから冷気が漏れている。',
    '遠くでモンスターの気配がする。',
    '足元に何かの痕跡が残っている。先人の苦闘の証か。',
    '松明が一瞬揺らめいた。何かが近くにいる。',
    '天井から水滴が落ちてきた。この階は湿気が多い。',
    '壁に誰かが残したメモがある。「ここでひきかえせ」と書いてある。',
    '地面に散乱した骨が、ここの危険さを物語っている。',
    '空気が淀んでいる。長い間、誰も通っていないのだろう。',
    '遠くから風が吹いてくる。どこかに出口があるのか...いや、ワナかもしれない。',
  ];
  return generic[floor % generic.length];
}

